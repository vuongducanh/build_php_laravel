    /**
     * login api
     *
     * @return \Illuminate\Http\Response
     */
    public function login(Request $request)
    {
        $credentials = $request->validate([
            'email' => 'required|email',
            'password' => 'required|string|min:6',
        ]);

        if (Auth::guard('web')->attempt($credentials)) {
            $user = Auth::guard('web')->user();
            $objToken = $user->createToken('API Access');
            $token = $objToken->accessToken;
            $expiration = $objToken->token->expires_at->diffInSeconds(now());
            $expiresAt = Carbon::parse($objToken->token->expires_at)->toDateTimeString();

            return response()->json([
                'user' => $user,
                'token' => $token,
                'msg' => "login success",
                'token_type' => "Bearer",
                'expires_in' => $expiration,
                'expires_at' => $expiresAt
            ], 200);
        } else {
            return response()->json(['msg' => "login fail"], 400);
        }

    }

    /**
     * Register api
     *
     * @return \Illuminate\Http\Response
     */
    public function register(Request $request)
    {
        $validator = Validator::make($request->all(),
            [
                'name' => 'required|string|between:2,100',
                'email' => 'required|string|email|max:100|unique:users',
                'password' => 'required|string|min:6',
            ]
        );

        if($validator->fails()){
            return response()->json($validator->errors()->toJson(), 400);
        }

        $user = User::create(array_merge(
            $validator->validated(),
            ['password' => bcrypt($request->password)]
        ));

        return response()->json([
            'message' => 'User successfully registered',
            'user' => $user,
        ], 201);
    }
